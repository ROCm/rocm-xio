#!/bin/bash
# Generate all endpoint-related files

ENDPOINTS_DIR="$1"
REGISTRY_OUTPUT="$2"
INCLUDES_OUTPUT="$3"

if [ -z "$ENDPOINTS_DIR" ] || [ -z "$REGISTRY_OUTPUT" ] || \
   [ -z "$INCLUDES_OUTPUT" ]; then
  echo "Usage: $0 <endpoints_dir> <registry_output> <includes_output>"
  exit 1
fi

# Find all endpoint directories (exclude common subdirectory)
ENDPOINTS=$(find "$ENDPOINTS_DIR" -mindepth 1 -maxdepth 1 -type d -not -name "common" -exec basename {} \; | sort)

# Helper function to convert endpoint name to define name
get_endpoint_define() {
  local ep="$1"
  case "$ep" in
    test-ep)
      echo "TEST"
      ;;
    nvme-ep)
      echo "NVME"
      ;;
    nvme-simple-ep)
      echo "NVME_SIMPLE"
      ;;
    sdma-ep)
      echo "SDMA"
      ;;
    rdma-ep)
      echo "RDMA"
      ;;
    *)
      # Default: convert nvme-simple-ep -> NVME_SIMPLE_EP, then remove _EP
      echo "$ep" | sed 's/-ep$//' | sed 's/-/_/g' | tr '[:lower:]' '[:upper:]'
      ;;
  esac
}

# Helper function to get endpoint description (matches hardcoded values
# in src/endpoints/common/axiio-endpoint.hip)
get_endpoint_description() {
  local ep="$1"
  case "$ep" in
    test-ep)
      echo "Test endpoint for development/testing"
      ;;
    nvme-ep)
      echo "nvme endpoint"
      ;;
    rdma-ep)
      echo "RDMA endpoint with multi-vendor support (MLX5, BNXT_RE, IONIC, PVRDMA)"
      ;;
    sdma-ep)
      echo "AMD SDMA Engine endpoint"
      ;;
    *)
      # Default fallback: convert endpoint name to description
      echo "$ep" | sed 's/-ep$/ endpoint/' | sed 's/-/ /g'
      ;;
  esac
}

# ============================================================================
# Generate endpoint registry (with enum)
# ============================================================================
cat > "$REGISTRY_OUTPUT" << 'REGISTRY_EOF'
/* Copyright (c) Advanced Micro Devices, Inc. All rights reserved.
 *
 * SPDX-License-Identifier: MIT
 *
 * AUTO-GENERATED FILE - DO NOT EDIT
 * Generated by scripts/generate-endpoint-files.sh
 */

#ifndef XIO_ENDPOINT_REGISTRY_GEN_H
#define XIO_ENDPOINT_REGISTRY_GEN_H

// Endpoint definitions

// Auto-generated EndpointType enum
enum class EndpointType {
REGISTRY_EOF

FIRST_ENUM=1
for ep in $ENDPOINTS; do
  EP_UPPER=$(echo "$ep" | tr '[:lower:]-' '[:upper:]_')
  if [ $FIRST_ENUM -eq 1 ]; then
    echo "  ${EP_UPPER}," >> "$REGISTRY_OUTPUT"
    FIRST_ENUM=0
  else
    echo "  ${EP_UPPER}," >> "$REGISTRY_OUTPUT"
  fi
done

cat >> "$REGISTRY_OUTPUT" << 'REGISTRY_EOF'
  UNKNOWN
};

// Auto-discovered endpoints (numeric defines)
REGISTRY_EOF

INDEX=0
for ep in $ENDPOINTS; do
  EP_UPPER=$(echo "$ep" | tr '[:lower:]-' '[:upper:]_')
  echo "#define ENDPOINT_${EP_UPPER} ${INDEX}" >> "$REGISTRY_OUTPUT"
  ((INDEX++))
done

echo "" >> "$REGISTRY_OUTPUT"
echo "// Endpoint metadata" >> "$REGISTRY_OUTPUT"
echo "#define ENDPOINT_REGISTRY_ENTRIES { \\" >> "$REGISTRY_OUTPUT"

FIRST=1
for ep in $ENDPOINTS; do
  DESC=$(get_endpoint_description "$ep")
  
  if [ $FIRST -eq 0 ]; then
    echo ", \\" >> "$REGISTRY_OUTPUT"
  fi
  FIRST=0
  
  EP_UPPER=$(echo "$ep" | tr '[:lower:]-' '[:upper:]_')
  echo -n "  {\"$ep\", \"$DESC\", EndpointType::${EP_UPPER}}" >> "$REGISTRY_OUTPUT"
done

cat >> "$REGISTRY_OUTPUT" << 'REGISTRY_EOF'
 \
}

#endif // XIO_ENDPOINT_REGISTRY_GEN_H
REGISTRY_EOF

echo "Generated endpoint registry: $REGISTRY_OUTPUT"

# ============================================================================
# Generate endpoint includes
# ============================================================================
cat > "$INCLUDES_OUTPUT" << 'INCLUDES_EOF'
/* Copyright (c) Advanced Micro Devices, Inc. All rights reserved.
 *
 * SPDX-License-Identifier: MIT
 *
 * AUTO-GENERATED FILE - DO NOT EDIT
 * Generated by scripts/generate-endpoint-files.sh
 */

// Include endpoint-specific headers to get their size definitions
INCLUDES_EOF

FIRST=1
for ep in $ENDPOINTS; do
  EP_UPPER=$(echo "$ep" | tr '[:lower:]-' '[:upper:]_')
  EP_DEFINE=$(get_endpoint_define "$ep")
  EP_HEADER="${ep}.h"
  SQE_SIZE_DEFINE="${EP_UPPER}_SQE_SIZE"
  CQE_SIZE_DEFINE="${EP_UPPER}_CQE_SIZE"
  
  if [ $FIRST -eq 1 ]; then
    echo "#if defined(AXIIO_ENDPOINT_${EP_DEFINE})" >> "$INCLUDES_OUTPUT"
    FIRST=0
  else
    echo "#elif defined(AXIIO_ENDPOINT_${EP_DEFINE})" >> "$INCLUDES_OUTPUT"
  fi
  
  echo "#include \"${EP_HEADER}\"" >> "$INCLUDES_OUTPUT"
  echo "#define XIO_SQE_SIZE ${SQE_SIZE_DEFINE}" >> "$INCLUDES_OUTPUT"
  echo "#define XIO_CQE_SIZE ${CQE_SIZE_DEFINE}" >> "$INCLUDES_OUTPUT"
done

cat >> "$INCLUDES_OUTPUT" << 'INCLUDES_EOF'
#else
// Default sizes if no specific endpoint is defined
#define XIO_SQE_SIZE 64
#define XIO_CQE_SIZE 16
#endif
INCLUDES_EOF

echo "Generated endpoint includes: $INCLUDES_OUTPUT"

