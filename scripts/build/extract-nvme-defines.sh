        }
        skip_union = 0
        next
    }
    if (/^[ \t]*\}[ \t]*result[ \t]*;/) {
        skip_union = 0
        next
    }
    next
}
in_struct == 1 {
    # Stop at struct closing brace
    if (/^};/) {
        exit
    }
    # Skip macros, enums, structs, unions, and comments
    if (/^[ \t]*#/ || /^[ \t]*enum/ || /^[ \t]*struct/ || /^[ \t]*union/ || /^[ \t]*\/\*/ || /^[ \t]*\*/) {
        next
    }
    # Skip empty lines
    if (/^[ \t]*$/) {
        next
    }
    # Convert kernel types
    gsub(/__u8/, "uint8_t")
    gsub(/__u16/, "uint16_t")
    gsub(/__u32/, "uint32_t")
    gsub(/__u64/, "uint64_t")
    gsub(/__le16/, "uint16_t")
    gsub(/__le32/, "uint32_t")
    gsub(/__le64/, "uint64_t")
    gsub(/__be16/, "uint16_t")
    gsub(/__be32/, "uint32_t")
    gsub(/__be64/, "uint64_t")
    # Clean up formatting - convert tabs to spaces and normalize
    gsub(/\t/, "  ")
    gsub(/[ \t]+/, " ")
    gsub(/^ /, "")
    # Remove C-style comments at end of line
    gsub(/[ \t]*\/\*.*\*\/[ \t]*$/, "")
    if ($0 != "" && !/^\/\//) print "  " $0
}' "$INPUT_FILE" >> "$OUTPUT_FILE"

