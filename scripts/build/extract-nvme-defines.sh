set -e

INPUT_FILE="$1"
OUTPUT_FILE="$2"

if [ -z "$INPUT_FILE" ] || [ -z "$OUTPUT_FILE" ]; then
    echo "Usage: $0 <input-linux-nvme.h> <output-generated.h>"
    exit 1
fi

if [ ! -f "$INPUT_FILE" ]; then
    echo "Error: Input file not found: $INPUT_FILE"
    exit 1
fi

# Create output directory if needed
mkdir -p "$(dirname "$OUTPUT_FILE")"

# Generate header file with extracted values
cat > "$OUTPUT_FILE" << 'EOF'
/* Auto-generated from Linux kernel header: src/include/external/linux-nvme.h
 * DO NOT EDIT MANUALLY - This file is generated by scripts/build/extract-nvme-defines.sh
 * To regenerate: run 'make fetch-nvme-headers' or rebuild the project
 */

#ifndef NVME_EP_GENERATED_H
#define NVME_EP_GENERATED_H

// Queue entry size exponents (from kernel: NVME_NVM_IOSQES, NVME_NVM_IOCQES)
EOF

# Extract NVME_NVM_IOSQES and NVME_NVM_IOCQES
grep -E "^#define NVME_NVM_IOSQES|^#define NVME_NVM_IOCQES" "$INPUT_FILE" | \
    sed 's/^#define /#define /' >> "$OUTPUT_FILE"

# Extract NVME_IDENTIFY_DATA_SIZE (4096) - also used as NVMe PRP page size
cat >> "$OUTPUT_FILE" << 'EOF'

// NVMe page size (from kernel: NVME_IDENTIFY_DATA_SIZE)
// This is the standard NVMe page size used for PRP (Physical Region Page) entries
// and identify data structures. While named for identify data, it represents
// the standard 4KB page size used throughout NVMe.
EOF

# Extract the value from NVME_IDENTIFY_DATA_SIZE and define NVME_PAGE_SIZE
grep -E "^#define NVME_IDENTIFY_DATA_SIZE" "$INPUT_FILE" | \
    sed 's/^#define NVME_IDENTIFY_DATA_SIZE/#define NVME_PAGE_SIZE/' >> "$OUTPUT_FILE"

cat >> "$OUTPUT_FILE" << 'EOF'

// Command opcodes (from kernel: enum nvme_opcode)
enum {
EOF

# Extract nvme_opcode enum values (only the ones we use)
# Filter for specific opcodes we need
awk '/^enum nvme_opcode \{/,/^\};/ {
    if (/^enum nvme_opcode \{/) next
    if (/^\};/) exit
    # Extract enum entries we use
    if (/nvme_cmd_flush|nvme_cmd_write|nvme_cmd_read|nvme_cmd_write_uncor|nvme_cmd_compare|nvme_cmd_write_zeroes|nvme_cmd_dsm|nvme_cmd_verify|nvme_cmd_resv_register|nvme_cmd_resv_report|nvme_cmd_resv_acquire|nvme_cmd_resv_release/) {
        gsub(/^\s+/, "")
        gsub(/,\s*$/, "")
        if ($0 != "" && !/^\/\//) print "  " $0 ","
    }
}' "$INPUT_FILE" >> "$OUTPUT_FILE"

# Remove trailing comma from last enum entry
sed -i '$ s/,$//' "$OUTPUT_FILE"

cat >> "$OUTPUT_FILE" << 'EOF'
};

// Admin command opcodes (from kernel: enum nvme_admin_opcode)
enum {
EOF

# Extract nvme_admin_opcode enum values (only the ones we use)
awk '/^enum nvme_admin_opcode \{/,/^\};/ {
    if (/^enum nvme_admin_opcode \{/) next
    if (/^\};/) exit
    # Extract enum entries we use
    if (/nvme_admin_delete_sq|nvme_admin_create_sq|nvme_admin_get_log_page|nvme_admin_delete_cq|nvme_admin_create_cq|nvme_admin_identify|nvme_admin_abort_cmd|nvme_admin_set_features|nvme_admin_get_features|nvme_admin_async_event|nvme_admin_ns_mgmt|nvme_admin_activate_fw|nvme_admin_download_fw|nvme_admin_ns_attach|nvme_admin_format_nvm|nvme_admin_security_send|nvme_admin_security_recv/) {
        gsub(/^\s+/, "")
        gsub(/,\s*$/, "")
        if ($0 != "" && !/^\/\//) print "  " $0 ","
    }
}' "$INPUT_FILE" >> "$OUTPUT_FILE"

# Remove trailing comma from last enum entry
sed -i '$ s/,$//' "$OUTPUT_FILE"

cat >> "$OUTPUT_FILE" << 'EOF'
};

// Status code types (from kernel: NVME_SCT_*)
// These are the SCT values (0-7) used in status field bits 9-11
#define NVME_SCT_GENERIC 0x0
#define NVME_SCT_CMD_SPECIFIC 0x1
#define NVME_SCT_MEDIA_ERROR 0x2
#define NVME_SCT_VENDOR 0x7

// Status codes (from kernel: enum values NVME_SC_*)
// Extract common status codes we use
EOF

# Extract NVME_SC_* defines from the enum (only the ones we use)
# Find the enum block that contains NVME_SC_* values
awk '/^\tNVME_SC_SUCCESS/,/^\};/ {
    # Only extract the specific status codes we use (exclude others)
    if (/^\tNVME_SC_SUCCESS\s*=|^\tNVME_SC_INVALID_OPCODE\s*=|^\tNVME_SC_INVALID_FIELD\s*=|^\tNVME_SC_CMDID_CONFLICT\s*=|^\tNVME_SC_DATA_XFER_ERROR\s*=|^\tNVME_SC_POWER_LOSS\s*=|^\tNVME_SC_INTERNAL\s*=|^\tNVME_SC_ABORT_REQ\s*=|^\tNVME_SC_ABORT_QUEUE\s*=|^\tNVME_SC_FUSED_FAIL\s*=|^\tNVME_SC_FUSED_MISSING\s*=|^\tNVME_SC_INVALID_NS\s*=|^\tNVME_SC_CMD_SEQ_ERROR\s*=|^\tNVME_SC_LBA_RANGE\s*=|^\tNVME_SC_CAP_EXCEEDED\s*=|^\tNVME_SC_NS_NOT_READY\s*=|^\tNVME_SC_RESERVATION_CONFLICT\s*=/) {
        # Convert enum entry to #define
        gsub(/^\t/, "")
        gsub(/,\s*$/, "")
        if ($0 != "" && !/^\/\//) {
            # Convert "NVME_SC_SUCCESS = 0x0," to "#define NVME_SC_SUCCESS 0x0"
            gsub(/\s*=\s*/, " ")
            print "#define " $0
        }
    }
    if (/^\};/) exit
}' "$INPUT_FILE" >> "$OUTPUT_FILE"

cat >> "$OUTPUT_FILE" << 'EOF'

// Structures (adapted from kernel: struct nvme_common_command and struct nvme_completion)
// Kernel structures use __le32/__le64 and struct_group which don't work in HIP device code,
// so we extract and adapt them to use standard types

// Submission Queue Entry (from kernel: struct nvme_common_command)
// We split cdw2[2] into cdw2 and cdw3 for easier access
struct nvme_sqe {
  uint8_t opcode;
  uint8_t flags;
  uint16_t command_id;
  uint32_t nsid;
  uint32_t cdw2;
  uint32_t cdw3;
  uint64_t metadata;
  union {
    struct {
      uint64_t prp1;
      uint64_t prp2;
    } prp;
    struct {
      uint64_t addr;
      uint32_t length;
      uint32_t key_type;
    } sgl;
  } dptr;
  uint32_t cdw10;
  uint32_t cdw11;
  uint32_t cdw12;
  uint32_t cdw13;
  uint32_t cdw14;
  uint32_t cdw15;
} __attribute__((packed));

// Completion Queue Entry (from kernel: struct nvme_completion)
struct nvme_cqe {
  union {
    uint32_t result;
    struct {
      uint32_t cdw0;
    };
  };
  uint32_t rsvd;
  uint16_t sq_head;
  uint16_t sq_id;
  uint16_t command_id;
  uint16_t status;
} __attribute__((packed));

#endif // NVME_EP_GENERATED_H
EOF

echo "Generated NVMe defines: $OUTPUT_FILE"

