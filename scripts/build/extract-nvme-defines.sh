        print "  uint32_t cdw2;"
        print "  uint32_t cdw3;"
        next
    }
    
    # Clean up and print - convert tabs to spaces
    gsub(/\t/, "  ")
    gsub(/[ \t]+/, " ")
    gsub(/^ /, "")
    if ($0 != "" && !/^\/\//) print "  " $0
}' "$INPUT_FILE" >> "$OUTPUT_FILE"

