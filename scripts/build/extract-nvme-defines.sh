# Function to convert kernel types to HIP-friendly types
convert_kernel_types() {
    sed -E \
        -e 's/\b__u8\b/uint8_t/g' \
        -e 's/\b__u16\b/uint16_t/g' \
        -e 's/\b__u32\b/uint32_t/g' \
        -e 's/\b__u64\b/uint64_t/g' \
        -e 's/\b__le16\b/uint16_t/g' \
        -e 's/\b__le32\b/uint32_t/g' \
        -e 's/\b__le64\b/uint64_t/g' \
        -e 's/\b__be16\b/uint16_t/g' \
        -e 's/\b__be32\b/uint32_t/g' \
        -e 's/\b__be64\b/uint64_t/g' \
        -e 's/\b__s8\b/int8_t/g' \
        -e 's/\b__s16\b/int16_t/g' \
        -e 's/\b__s32\b/int32_t/g' \
        -e 's/\b__s64\b/int64_t/g'
}

# Function to extract and convert a structure
# Usage: extract_struct "struct_name" "comment"
extract_struct() {
    local struct_name="$1"
    local comment="$2"
    local start_pattern="^struct ${struct_name}[[:space:]]*\{"
    local in_struct=0
    local brace_depth=0
    local output=""
    
    # Find the structure
    while IFS= read -r line; do
        if [[ $line =~ $start_pattern ]]; then
            in_struct=1
            output="// ${comment}\n"
            output+="struct ${struct_name} {\n"
            # Count opening brace
            brace_depth=$(echo "$line" | grep -o '{' | wc -l)
            continue
        fi
        
        if [ $in_struct -eq 1 ]; then
            # Count braces
            open_braces=$(echo "$line" | grep -o '{' | wc -l)
            close_braces=$(echo "$line" | grep -o '}' | wc -l)
            brace_depth=$((brace_depth + open_braces - close_braces))
            
            # Skip struct_group and its contents
            if [[ $line =~ struct_group ]]; then
                # Skip until matching closing paren
                local paren_depth=1
                while IFS= read -r group_line; do
                    open_parens=$(echo "$group_line" | grep -o '(' | wc -l)
                    close_parens=$(echo "$group_line" | grep -o ')' | wc -l)
                    paren_depth=$((paren_depth + open_parens - close_parens))
                    if [ $paren_depth -eq 0 ]; then
                        break
                    fi
                done
                continue
            fi
            
            # Convert kernel types and clean up
            converted_line=$(echo "$line" | convert_kernel_types)
            
            # Remove tabs and normalize whitespace
            converted_line=$(echo "$converted_line" | sed 's/\t/  /g' | sed 's/^[[:space:]]*/  /')
            
            # Handle array declarations: cdw2[2] -> cdw2, cdw3 (for nvme_common_command)
            if [[ $struct_name == "nvme_common_command" ]] && [[ $converted_line =~ cdw2\[2\] ]]; then
                output+="  uint32_t cdw2;\n"
                output+="  uint32_t cdw3;\n"
            else
                output+="$converted_line\n"
            fi
            
            # Check if we've closed all braces
            if [ $brace_depth -eq 0 ]; then
                output+="} __attribute__((packed));\n"
                echo -e "$output"
                return 0
            fi
        fi
    done < "$INPUT_FILE"
    
    return 1
}

