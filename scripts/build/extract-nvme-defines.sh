        }
        next
    }
    
    # Handle union nvme_data_ptr - expand it inline
    if (/union nvme_data_ptr/) {
        print "  union {"
        print "    struct {"
        print "      uint64_t prp1;"
        print "      uint64_t prp2;"
        print "    } prp;"
