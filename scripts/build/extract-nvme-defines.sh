        # Convert enum entry to #define
        gsub(/^[ \t]+/, "")
        gsub(/,[ \t]*$/, "")
        if ($0 != "" && !/^\/\//) {
            # Convert "NVME_SC_SUCCESS = 0x0," to "#define NVME_SC_SUCCESS 0x0"
            gsub(/[ \t]*=[ \t]*/, " ")
